CREATE OR REPLACE FUNCTION DBVESTIB.F_INSERE_DIGITO(TELEFONE IN VARCHAR2) RETURN VARCHAR2 IS
	STEL VARCHAR2(30);
BEGIN
	STEL := TELEFONE;
	IF (DBSIAF.F_SO_NUMERO(STEL) IS NULL)
	   OR (LENGTH(DBSIAF.F_SO_NUMERO(STEL)) < 10) THEN
		WHILE (LENGTH(DBSIAF.F_SO_NUMERO(STEL)) < 10)
			  OR (DBSIAF.F_SO_NUMERO(STEL) IS NULL) LOOP
			STEL := '9' || STEL;
		END LOOP;
	END IF;
	RETURN(DBSIAF.F_SO_NUMERO(STEL));

END;

